function [X,tnn] = Log_prox_tnn(Y,rho)
% The proximal operator of the tensor nuclear norm of a 3 way tensor
%
% min_X rho*||X||_*+0.5*||X-Y||_F^2
%
% Y     -    n1*n2*n3 tensor
%
% X     -    n1*n2*n3 tensor
% tnn   -    tensor nuclear norm of X
% trank -    tensor tubal rank of X
%
% version 2.1 - 14/06/2018

[n1,n2,n3] = size(Y);
X = zeros(n1,n2,n3);
Y = fft(Y,[],3);
tnn = 0;
% trank = 0;
        
% first frontal slice
X(:,:,1) = WNNM( Y(:,:,1), rho, 1);
% i=2,...,halfn3
halfn3 = round(n3/2);
for i = 2 : halfn3
   X(:,:,i) = WNNM( Y(:,:,i), rho, 1);
    X(:,:,n3+2-i) = conj(X(:,:,i));
end

% if n3 is even
if mod(n3,2) == 0
    i = halfn3+1;
    X(:,:,i) = WNNM( Y(:,:,i), rho, 1);
end
tnn = tnn/n3;
X = ifft(X,[],3);
%
end
